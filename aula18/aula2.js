let fruits = ['banana', 'uva', 'morango', 'melancia'];

//ordenando em ordem crescente
fruits.sort();

console.log(fruits);

//invertendo a ordem do array

let fruits2 = ['banana', 'uva', 'morango', 'melancia'];
fruits2.reverse();

console.log(fruits2);

//caso queira colocar na ordem decrescente usar o sort e depois o reverse

fruits.reverse();

console.log(fruits);